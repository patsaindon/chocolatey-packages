$checksum     = '083ccc7002dffd71b2216d7eb657d30f80db51e1d28bcbeaa5af70d9d77c729d'
$checksumType = 'sha256'

$packageArgs = @{
  packageName    = $env:ChocolateyPackageName
  fileType      = 'exe'
  url            = $url
  checksum       = $checksum
  checksumType   = $checksumType
